Begin["`Private`"];


IntersectSubspaces[n_,{}]:=IdentityMatrix[n]


IntersectSubspaces[n_,{basis_}]:=basis


IntersectSubspaces[n_,{{},_}]:={}
IntersectSubspaces[n_,{_,{}}]:={}


IntersectSubspaces[n_,{basis1_,basis2_}]:=NullSpace[Join@@(NullSpace/@{basis1,basis2})]


IntersectSubspaces[n_,{basis1_,basis2_,bases__}]:=IntersectSubspaces[n,{basis1,IntersectSubspaces[n,{basis2,bases}]}]


