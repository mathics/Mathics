IntersectSubspaces::usage="IntersectSubspaces[n,\!\(\*SubscriptBox[\"\[Beta]\", \"1\"]\),\!\(\*SubscriptBox[\"\[Beta]\", \"2\"]\),...] gives a basis for the intersection of the subspaces of \!\(\*SuperscriptBox[\"A\", 
